"""
정수 N이 주어지면, 영화 제목에 "666"이 들어가는 N번째 영화의 제목을 출력한다.

예제 1:
입력: 2
출력: 1666

예제 2:
입력: 3
출력: 2666

예제 3:
입력: 6
출력: 5666

예제 4:
입력: 187
출력: 66666

예제 5:
입력: 500
출력: 166699

제약 조건:
N은 10,000보다 작거나 같은 자연수이다.

풀이:
1. "666"이 들어가야 하므로 666부터 시작해도 상관없다.
2. "666"이 문자열안에 있는 확인한다.
"""

import sys

# [입력 처리]
n = int(sys.stdin.readline())

# [핵심 로직]
num = 666
while n != 0:
    if "666" in str(num):
        n -= 1

    num += 1

# [결과 출력]
print(num-1)
