"""
Rev(Rev(X) + Rev(Y))를 계산한다.
Rev(X): 모든 자리수를 역순으로 만든다.

예제 1:
입력: 123 100
출력: 223

예제 2:
입력: 111 111
출력: 222

예제 3:
입력: 5 5
출력: 1

예제 4:
입력: 1000 1
출력: 2

예제 5:
입력: 456 789
출력: 1461

제약 조건:
X와 Y는 1,000보다 작거나 같은 자연수이다.
"""

import sys

# [입력 처리]
x, y = sys.stdin.readline().split()

# [핵심 로직]
res = int(x[::-1]) + int(y[::-1])

# [결과 출력]
print(int(str(res)[::-1]))
