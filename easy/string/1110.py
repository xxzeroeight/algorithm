"""
문자열이 주어지면, 연산 후 원래 수로 돌아오는 사이클의 수를 센다.

예제 1:
입력: 26
출력: 4

예제 2:
입력: 55
출력: 3

예제 3:
입력: 1
출력: 60

예제 4:
입력: 0
출력: 1

예제 5:
입력: 71
출력: 12

제약 조건:
N은 0보다 크거나 같고, 99보다 작거나 같은 정수이다.
"""

import sys

# [입력 처리]
n = int(sys.stdin.readline())

# [핵심 로직]
cnt = 0
origin = n
while 1:
    s = n//10 + n%10

    n = n%10*10 + s%10

    cnt += 1

    if origin == n:
        break

# [결과 출력]
print(cnt)
