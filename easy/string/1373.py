# ===== 해결 전략 =====
# 아이디어: 2진수 -> 10진수 -> 8진수

# ===== 입력 처리 =====
bi = sys.stdin.readline().rstrip()

# ===== 핵심 로직 =====
de = int(bi, 2)
result = oct(de)

# ===== 결과 출력 =====
print(result[2:])

